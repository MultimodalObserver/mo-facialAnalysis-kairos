    private void acceptButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_acceptButtonMouseClicked
        
        if(this.configurationField.getText().equals("")||this.configurationField.getText()==null ||
           this.keyField.getText().equals("") || this.keyField.getText()==null){
            JOptionPane.showMessageDialog(null,"Porfavor complete todos los campos");
        }
        else
        {
            this.accepted = true;
            this.analyzer = new KairosAnalyser("https://api.kairos.com/v2/media",
                                                           this.app_idField.getText(),
                                                           this.keyField.getText());        
            this.dispose();
        }
    }//GEN-LAST:event_acceptButtonMouseClicked

